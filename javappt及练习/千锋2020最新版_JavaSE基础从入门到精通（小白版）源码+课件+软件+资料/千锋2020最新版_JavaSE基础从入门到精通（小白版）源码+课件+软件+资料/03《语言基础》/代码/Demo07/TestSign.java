public class TestSign{
	
	public static void main(String[] args){
		
		char c1 = '\'';
		
		System.out.println(c1);
		
		
		System.out.println("\"");
		
		System.out.println("Hello\tWorld");
		
		System.out.println("Hello\nWorld");
		
		System.out.println("\\");
		
		System.out.println("u0041");
		
		System.out.println("\u0041");
	}
}